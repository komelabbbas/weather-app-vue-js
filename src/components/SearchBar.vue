<template>
  <div class="pt-4 mb-8 relative">
    <input type="text" v-model="searchQuery" @input="getSearchResults" placeholder="Search for a city or state"
      class="py-2 px-1 w-full bg-transparent border-b focus:border-weather-secondary focus:outline-none focus:shadow-[0px_1px_0_0_#004E71]" />

    <ul class="absolute bg-weather-secondary text-white w-full shadow-md py-2 px-1 top-[66px]" v-if="mapboxSearchResults">
      <p class="py-2" v-if="searchError">
        Sorry, something went wrong, please try again.
      </p>
      <p class="py-2" v-if="!searchError && mapboxSearchResults.length === 0">
        No results match your query, try a different term.
      </p>
      <template v-else>
        <li v-for="searchResult in mapboxSearchResults" :key="searchResult.id" class="py-2 cursor-pointer"
          @click="$emit('previewCity', searchResult)">
          {{ searchResult.place_name }}
        </li>
      </template>
    </ul>
  </div>
  <div class="flex flex-col gap-4">
    <p>
      No locations added. To start tracking a location, search in
      the field above.
    </p>
  </div>
</template>

<script setup>
import axios from "axios";

defineEmits(["previewCity"]);

const searchQuery = ref("");
const queryTimeout = ref(null);
const mapboxSearchResults = ref(null);
const searchError = ref(null);

const getSearchResults = () => {
  clearTimeout(queryTimeout.value);
  queryTimeout.value = setTimeout(async () => {
    if (searchQuery.value !== "") {
      try {
        const result = await axios.get(
          `${import.meta.env.VITE_MAP_URL}/geocoding/v5/mapbox.places/${searchQuery.value}.json?access_token=${import.meta.env.VITE_MAP_ID}&types=place`
        );
        mapboxSearchResults.value = result.data.features;
      } catch {
        searchError.value = true;
      }

      return;
    }
    mapboxSearchResults.value = null;
  }, 300);
};
</script>
